#include "SDUtils.hpp"

String CSDUtils::gif_paths[MAX_GIFS];

String CSDUtils::bmp_paths[MAX_BMPS];

bool CSDUtils::Initialise() {
    if (!SD.begin()) {
        Serial.println("Card Mount Failed");
        return false;
    }
    uint8_t cardType = SD.cardType();
    
    if (cardType == CARD_NONE) {
        Serial.println("No SD card attached");
        return false;
    }

    Serial.print("SD Card Type: ");
    if (cardType == CARD_MMC) {
        Serial.println("MMC");
    } else if (cardType == CARD_SD) {
        Serial.println("SDSC");
    } else if (cardType == CARD_SDHC) {
        Serial.println("SDHC");
    } else {
        Serial.println("UNKNOWN");
    }

    uint64_t cardSize = SD.cardSize() / (1024 * 1024);
    Serial.printf("SD Card Size: %lluMB\n", cardSize);

    // Make sure the file structure we want is there
    createDir(SD, "/data");
    createDir(SD, "/data/bitmaps");
    createDir(SD, "/data/gifs");

    GetGifs(SD);
    GetBMPs(SD);


    return true;
}

void CSDUtils::GetGifs(fs::FS &fs){

    File gifsDir = fs.open("/data/gifs");
    if (!gifsDir){
        return;
    }

    for(int i = 0; i < MAX_GIFS; i++){
        File file = gifsDir.openNextFile();
        if (!file){
            break;
        }
        gif_paths[i] = file.name();

    }

}

void CSDUtils::GetBMPs(fs::FS &fs){

    File bmpDir = fs.open("/data/bitmaps");
    if (!bmpDir){
        return;
    }

    for(int i = 0; i < MAX_GIFS; i++){
        File file = bmpDir.openNextFile();
        if (!file){
            break;
        }
        bmp_paths[i] = file.name();

    }

}

void CSDUtils::listDir(fs::FS &fs, const char * dirname, uint8_t levels){
    Serial.printf("Listing directory: %s\n", dirname);

    File root = fs.open(dirname);
    if(!root){
        Serial.println("Failed to open directory");
        return;
    }
    if(!root.isDirectory()){
        Serial.println("Not a directory");
        return;
    }

    File file = root.openNextFile();
    while(file){
        if(file.isDirectory()){
            Serial.print("  DIR : ");
            Serial.println(file.name());
            if(levels){
                listDir(fs, file.name(), levels -1);
            }
        } else {
            Serial.print("  FILE: ");
            Serial.print(file.name());
            Serial.print("  SIZE: ");
            Serial.println(file.size());
        }
        file = root.openNextFile();
    }
}

